#include "libft.h"

void	*ft_memset(void *dest, int c, size_t count)
{
	size_t			i;
	unsigned char	*tmp;

	tmp = dest;
	i = 0;
	while (i < count)
	{
		tmp[i] = (unsigned char)c;
		i++;
	}
	return (dest);
}
