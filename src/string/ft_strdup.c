#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*result;

	result = malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (!result)
		return (NULL);
	ft_strlcpy(result, src, ft_strlen(src) + 1);
	result[ft_strlen(src)] = '\0';
	return (result);
}
