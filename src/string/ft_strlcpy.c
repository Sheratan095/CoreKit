#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t sz)
{
	size_t	src_len;

	src_len = ft_strlen(src);
	if (src_len < sz)
		ft_memcpy(dst, src, src_len + 1);
	else if (sz != 0)
	{
		ft_memcpy(dst, src, sz -1);
		dst[sz - 1] = '\0';
	}
	return (src_len);
}
